const UUID_RE = /^[0-9a-f]{8}-[0-9a-f]{4}-[0-9a-f]{4}-[0-9a-f]{4}-[0-9a-f]{12}$/i;

/** Append a row to public.audit_log. Best-effort: failures are logged but do not throw.
 *  Non-UUID actor_id values are demoted to null + recorded under details.actor_label so
 *  the audit row still lands (e.g. for smoke tests or service identities without a UUID).
 */
export async function appendAuditLog(args: {
  action: string;
  entity_type?: string;
  entity_id?: string;
  actor_id?: string;
  actor_role?: "admin" | "reviewer" | "moderator" | "observer";
  details?: Record<string, unknown>;
}): Promise<void> {
  try {
    const sb = getSupabase();
    const validUuid = args.actor_id && UUID_RE.test(args.actor_id);
    const details: Record<string, unknown> = { ...(args.details ?? {}) };
    if (args.actor_id && !validUuid) details.actor_label = args.actor_id;
    const { error } = await sb.from("audit_log").insert({
      action: args.action,
      entity_type: args.entity_type ?? null,
      entity_id: args.entity_id ?? null,
      actor_id: validUuid ? args.actor_id : null,
      actor_role: args.actor_role ?? null,
      details,
    });
    if (error) console.warn("audit_log insert failed:", error.message);
  } catch (e) {
    console.warn("audit_log insert threw:", e);
  }
}

function rowToCoreOutput(v: VerdictRow): CoreOutput {
  return {
    case_id: v.case_id,
    methodology_hash: v.methodology_hash,
    anchors_hash: v.anchors_hash,
    prompt_hash: v.prompt_hash,
    input_hash: v.input_hash,
    model_id: v.model_id,
    block_code: v.block_id,
    nomination_code: "",
    l2: {
      criteria_scores: (v.criteria_scores ?? []).map((c) => ({
        criterion: c.criterion_id,
        score: c.score,
        rationale: c.rationale,
        evidence_ids: c.evidence_ids ?? [],
      })),
      block_score: v.total_score,
      total_score: v.total_score,
      award_level: v.award_level,
      one_paragraph_verdict: v.one_paragraph_verdict,
    },
    evidence: [],
    missing_evidence: v.missing_evidence ?? [],
    key_quotes: [],
    avatar_script: "",
    consistency_check_passed: v.consistency_check_passed,
  };
}

