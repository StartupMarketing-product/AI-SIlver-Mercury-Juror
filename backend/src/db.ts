/** Status of the avatar video pipeline for a given verdict. Tracked client-side
 *  via the verdict's approval_state and the presence of `output.avatar_script`
 *  / a future `avatar_video_url` column. Until the column exists, status is
 *  derived heuristically. */
export type AvatarStatus =
  | "scoring"
  | "awaiting_review"
  | "approved"
  | "rendering"
  | "ready"
  | "failed";

export async function listVerdicts(opts: { limit?: number; nomination?: string } = {}): Promise<
  Array<{
    evaluation_id: string;
    case_id: string;
    created_at: string;
    award_level: VerdictRow["award_level"];
    total_score: number;
    approval_state: VerdictRow["approval_state"];
    project_name?: string;
    nomination_code?: string;
    avatar_status: AvatarStatus;
    avatar_video_url: string | null;
    avatar_script: string | null;
    one_paragraph_verdict: string | null;
  }>
> {
  const sb = getSupabase();
  const limit = opts.limit ?? 200;
  let q = sb
    .from("verdicts")
    .select(
      "id, case_id, created_at, award_level, total_score, approval_state, one_paragraph_verdict, avatar_script, avatar_video_url, avatar_video_id, avatar_status, avatar_error, cases!inner(project_name, nomination_id)"
    )
    .order("created_at", { ascending: false })
    .limit(limit);
  // Server-side filter by nomination code (D01/D10/D13/D15) — Moderator
  // screens use this so they only see their category.
  if (opts.nomination) q = q.eq("cases.nomination_id", opts.nomination);
  const { data, error } = await q;
  if (error) throw new Error(`listVerdicts failed: ${error.message}`);
  return (data ?? []).map((r: any) => {
    // Use the persisted avatar_status if present; otherwise fall back to the
    // approval_state-derived heuristic for legacy rows that pre-date the
    // avatar columns.
    const persisted = r.avatar_status as AvatarStatus | null;
    let avatarStatus: AvatarStatus;
    if (persisted) {
      avatarStatus = persisted;
    } else if (r.approval_state === "approved") {
      avatarStatus = r.avatar_video_url ? "ready" : "approved";
    } else if (r.approval_state === "rejected") {
      avatarStatus = "failed";
    } else {
      avatarStatus = "awaiting_review";
    }
    return {
      evaluation_id: r.id,
      case_id: r.case_id,
      created_at: r.created_at,
      award_level: r.award_level,
      total_score: Number(r.total_score),
      approval_state: r.approval_state,
      project_name: r.cases?.project_name ?? undefined,
      nomination_code: r.cases?.nomination_id ?? undefined,
      avatar_status: avatarStatus,
      avatar_video_url: r.avatar_video_url ?? null,
      avatar_script: r.avatar_script ?? null,
      one_paragraph_verdict: r.one_paragraph_verdict ?? null,
    };
  });
}

